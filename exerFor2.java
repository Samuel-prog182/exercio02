public class exerFor2{
    public static void main(String[] args){
        System.out.println("Exercicio 02 - Imprima a soma de 1 a 1000");

       //Declaração de variaveis
        int soma = 0;
        int j;

         //Inicio do For
        for( j = 1; j <= 1000; j++){soma = soma + j;
        } //Fim do For
        System.out.println("A soma é: " + soma);  //Impressão do resultado
   }
}